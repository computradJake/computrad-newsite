                  <h2>The Challenge of 'Over-Resoureful' Students</h2>
                  <p>Like many successful UK schools, Greenford prides itself on a strong commitment to investing in ICT as a learning resource.  Indeed, each Greenford student benefits from a multitude of opportunities to access networked resources and to surf the Internet. In total, over 2,000 users (students and staff alike) enjoy these privileges, though all within the constraints of a carefully stipulated Internet safety policy.</p>
                  <p>A combination of alarming national press reports and anecdotal insights led Greenford to question whether their enforcement of these policies, which govern the ability of each user to access safe URLs and appropriate web-based applications/content, were as watertight as required.  Particular concern was raised over the capacity of individual users to access web-based applications capable of ‘bypassing’ firewall controls, particularly via the use of proxy programs such as Ultrasurf.  The issue compounded with the revelation that Greenford’s configuration of wireless users meant they do not need to be authenticated against Windows Active Directory (and thus be governed by Greenford’s own proxy controls) in order to reach the Internet.</p>
                  <p>“Essentially we had no control over wireless users and Active Directory users who would use their own proxy applications to bypass our controls,” said David Croft, Greenford High School Network Manager.  “Controlling what users can access is essential. We cannot compromise on anything less than total responsibility when students – many of whom are under the age of 18 – are accessing materials through our networks.”</p>
