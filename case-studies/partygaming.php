                  <p>DIGIPASS GO 6 is a small ultra-portable and user-friendly authentication device. At the push of the button an OTP is generated. This OTP is only valid for a limited period of time and replaces the use of a static password.</p>
                  <p>PartyGaming customers (PartyPoker, PartyCasino, PartyBingo and PartyGammon) register by downloading the PartyPoker software from the PartyGaming website. Once registered, they can purchase the fully customized PartySecure authenticator from the PartyGaming online store. Registered players can then type their user name and use their authenticator to generate a one-time password (OTP) to log-on to their account. The use of OTP allows PartyGaming to ensure that the gamer says who he is and protects him from password theft. The OTP is only valid for a limited time and it can only be used once. Thus it becomes impossible to hand over passwords to unauthorized users. Furthermore, thanks to its limited validity keyloggers will not be able to re-use the password when intercepting it online. Last, providing that security can also rime with usability, users who have forgotten their authenticator can request for an OTP to be delivered by SMS to their cell phone.</p>
