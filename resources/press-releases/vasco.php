			  <p>The fastest growing newcomer award went to Keyfort. Keyfort designs, implements and manages cloud computing solutions; from virtualization of servers in the server room to the provision of secure networks within buildings, across campuses and over the Internet to sites world wide. Keyfort uses this technology together with its networking expertise and service infrastructure to provide for the secure and efficient flow of information between staff, both in and out of the office for corporate, local government and SME organizations. Awarding Keyfort is based upon its excellent sales efforts throughout 2009. Keyfort are a long standing SonicWall reseller and have been able to target their customer base with the VASCO portfolio, focusing on VASCO solution bundle strategy. In 2009 Keyfort also became an IDENTIKEY Certified reseller, enabling them to offer their customer base an end-to-end authentication solution, including technical support and installation.</p>

